package model;

public class Receita {

    public static String Nome;
    public static Double Valor;

    public String getNome()
    {
        return Nome;
    }
    public void setNome(String nome)
    {
        Nome = nome;
    }
    public Double getValor()
    {
        return Valor;
    }
    public void setValor(Double valor)
    {
        Valor = valor;
    }

}
